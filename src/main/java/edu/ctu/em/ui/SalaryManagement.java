    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtIdNV, sb, "Employee ID cannot be null!");
        DataValidator.validateEmpty(txtFullnameNV, sb, "Fullname cannot be null!");
        DataValidator.validateEmpty(txtWorkTime, sb, "Work-time cannot be null!");
        if(getDateInput()==null) sb.append("Date is not valid!\n");
        if(Float.parseFloat(txtWorkTime.getText())<=0){
            sb.append("Work-time must be a positive numbers!\n");
            txtWorkTime.setBackground(Color.yellow);
        }else{
            txtWorkTime.setBackground(Color.white);
        }
        
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Error");
            return;
        }

        try {
            Shift s = new Shift();
            s.setMaNV(txtIdNV.getText());
            
            String date = getDateInput();
            s.setNgayLam(date);
            s.setThoiGian(Float.parseFloat(txtWorkTime.getText()));
            
            ShiftTool sTool = new ShiftTool();
            sTool.insert(s);
            

            JOptionPane.showMessageDialog(this, "New shift has been saved to database!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtIdNV, sb, "Must enter employee ID to update!");
        if(getDateInput()==null) sb.append("Date is not valid!\n");
        if(Float.parseFloat(txtWorkTime.getText())<=0){
            sb.append("Work-time must be a positive numbers!\n");
            txtWorkTime.setBackground(Color.yellow);
        }else{
            txtWorkTime.setBackground(Color.white);
        }
        
        if(sb.length()>0){
            MessageDialogHelper.showErrorDialog(this, sb.toString(), "Error");
            return;
        }

        if(JOptionPane.showConfirmDialog(this, "Do you want to update info of the shift?",
                "Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.NO_OPTION){
        return;
        }

        try {
            ShiftTool sTool = new ShiftTool();
            Shift s = sTool.findByDate(txtIdNV.getText(), getDateInput());
            s.setThoiGian(Float.parseFloat(txtWorkTime.getText()));
            sTool.update(s);

            JOptionPane.showMessageDialog(this, "The shift has been updated!");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

