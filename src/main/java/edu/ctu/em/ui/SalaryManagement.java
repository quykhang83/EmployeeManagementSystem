    private float loadAllShiftbyID(String maNV, String thangNamLam){
        float total = 0.0F;
        try {
            String sql = "select * from shift where maNV = ? and thangNamLam = ?";
            try(
                    Connection con = DatabaseHelper.openConnection();
                    PreparedStatement psmt = con.prepareStatement(sql);
                ){
                psmt.setString(1, maNV);
                psmt.setString(2, thangNamLam);
                ResultSet rs = psmt.executeQuery();
                tblModel.setRowCount(0);
                while(rs.next()){
                    String[] row = new String[]{
                        rs.getString("maCa"), rs.getString("ngayLam"), "" + rs.getFloat("thoiGian")
                    }; total+=rs.getFloat("thoiGian");
                    tblModel.addRow(row);
                }
                tblModel.fireTableDataChanged();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
        return total;
    }

